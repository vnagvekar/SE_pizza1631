
public class pizza {
	String name;
	float price;
	String ptype;
	public pizza(String name,float price,String ptype)
	{
		this.name=name;
		this.price=price;
		this.ptype=ptype;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}
	
	public String getPtype()
	{
		return ptype;
	}
	
	public void setPtype(String ptype)
	{
		this.ptype=ptype;
	}

			

}

